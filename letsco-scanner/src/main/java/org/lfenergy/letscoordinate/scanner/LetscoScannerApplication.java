package org.lfenergy.letscoordinate.scanner;

import org.lfenergy.letscoordinate.scanner.config.LetscoProperties;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.context.properties.EnableConfigurationProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.scheduling.annotation.EnableScheduling;
import org.springframework.web.client.RestTemplate;

@SpringBootApplication
@EnableScheduling
@EnableConfigurationProperties({LetscoProperties.class})
public class LetscoScannerApplication {

	public static void main(String[] args) {
		SpringApplication.run(LetscoScannerApplication.class, args);
	}

	@Bean
	public RestTemplate restTemplate() {
		return new RestTemplate();
	}

}
